arr1 = ['2','0','1','0','0']
arr2 = ['1','0','1','0','1']
arr3 = ['1','0','1','0','1']
arr4 = ['1','0','1','0','1']
arr5 = ['1','0','0','0','1']
maze = [arr1,arr2,arr3,arr4,arr5]

for rows in maze:
    print(rows)

row= 0
col= 0 

completed = 0

while completed == 0:
    direction = input()

    if direction == "right":
        if maze[col][row + 1] == '0':
            maze[col][row] = '0'
            row = row + 1
            col = col
            maze[col][row] = '2'
            for rows in maze:
                print(rows)
        else:
            print("cant go right")

    if direction == "down":
        if maze[col + 1][row] == '0':
            maze[col][row] = '0'
            row = row
            col = col + 1
            maze[col][row] = '2'
            for rows in maze:
                print(rows)
        else:
            print("cant go down")

    if direction == "left":
        if maze[col][row - 1] == '0':
            maze[col][row] = '0'
            row = row - 1
            col = col
            maze[col][row] = '2'
            for rows in maze:
                print(rows)
        else:
            print("cant go left")

    if direction == "up":
        if maze[col - 1][row] == '0':
            maze[col][row] = '0'
            row = row
            col = col - 1
            maze[col][row] = '2'
            for rows in maze:
                print(rows)
        else:
            print("cant go up")

    if maze[0][4] == '2':
        completed = 1
        print("Congradulations you won!!")

